#pragma mark - Navigation

// In a story board-based application, you will often want to do a little preparation before navigation
- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    if([[segue identifier] isEqualToString:@"ExistingItem"]){
        NSString * path = [[self documents] objectAtIndex:[self tableView].indexPathForSelectedRow.row];
        PCViewController * c = (PCViewController *) [segue destinationViewController];
        c.path = [[self docDir] stringByAppendingPathComponent:path];
    }
}

